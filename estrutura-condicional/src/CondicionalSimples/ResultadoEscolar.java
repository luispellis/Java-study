public class ResultadoEscolar {

    /* Algumas vezes o nosso programa deverá seguir mais de uma jornada de execução condicionado a uma regra de negócio, este cenário
     * é denominado Estrutura Condicional Composta. Exemplo abaixo: 
     */
    
    public static void main(String[] args) {
        
         int nota = 6;

         if(nota >= 7 )
            System.out.println("Aprovado!");

        else 
            System.out.println("Reprovado!");
    }

}
